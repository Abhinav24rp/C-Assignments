#include <stdio.h>

int main() {

    int a  = 4;
    int b = 2;

    int sum = a + b;
    int subtraction = a - b;
    int product = a * b;
    int devide = a / b;
    int remainder = a % b;

    printf("Sum is:- %d \n", sum);
    printf("Subtraction is:- %d \n", subtraction);
    printf("Product is:- %d \n", product);
    printf("Devision is:- %d \n", devide);
    printf("Remainder is:- %d \n", remainder);
    return 0;
}